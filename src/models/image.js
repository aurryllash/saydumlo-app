const mongoose = require('mongoose')
const joi = require('joi')

const imageSchema = mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    }
})

module.exports.Image = mongoose.model('Image', imageSchema);